    private void txtAnalizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtAnalizarActionPerformed
        // TODO add your handling code here:
        File archivo = new File("archivo.txt");
        PrintWriter escribir;
        try {
            escribir = new PrintWriter(archivo);
            escribir.print(txtEntrada.getText());
            escribir.close();
        } catch (FileNotFoundException ex) {
            Logger.getLogger(FrmPrincipal.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        try {
            Reader lector = new BufferedReader(new FileReader("archivo.txt"));
            Lexer lexer = new Lexer(lector);
            String resultado = "";
            while(true){
                Tokens tokens = lexer.yylex();
                if(tokens == null){
                    resultado += "FIN";
                    txtResultado.setText(resultado);
                    return;
                }
                
                switch(tokens){
                    case ERROR:
                        resultado += "el simbolo no esta definido\n";
                        break;
                    case Identificador:            
                        resultado += lexer.lexeme + ": Es un " + tokens + "\n\n";
                        break;
                    case Numero:            
                        resultado += lexer.lexeme + ": Es un " + tokens + "\n\n";
                        break;
                    case Reservadas:            
                        resultado += lexer.lexeme + ": Es una de las palabras " + tokens + "\n\n";
                        break;
                    default:
                        resultado += "signo de: " + tokens + "\n\n";
                        break;
                }
            }
        } catch (FileNotFoundException ex) {
            Logger.getLogger(FrmPrincipal.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(FrmPrincipal.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        
    }//GEN-LAST:event_txtAnalizarActionPerformed

